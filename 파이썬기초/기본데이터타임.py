# 기본데이터 타입

a = 7
b = 7.7777
c = True
d = False
e = '문자열'

type(a)

print(type(a))
print(type(b))
print(type(c))
print(type(d))
print(type(e))

# 파이썬은 나눈 결과값이 소수점이면 자동으로 형변화가 일어난다.
print(2/4)
print(type(2/4))

# 제곱 , 몫, 나머지

print(7**4)  # 7에 4승
print(9//4)  # 몫은 2
print(9 % 4)  # 나머지 1

# 반올림 (숫자 , 표시되는 숫자 자리수 )

print(round(3.141592, 2))  # = 3.14
